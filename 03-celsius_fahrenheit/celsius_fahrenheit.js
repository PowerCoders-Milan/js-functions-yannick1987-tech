// define a function multiplyByNineFifths(number)
function multiplyByNineFifths(number) {
    //return number*(9/5)
    return number * (9 / 5);
}
// define a function getFahrenheit(celsius)
function getFahrenheit(celsius) {
    // return multiplyByNineFifths(celsius) + 32;
    return multiplyByNineFifths(celsius) + 32;
}
 // call getFahrenheit();
